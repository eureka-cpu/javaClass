package Exercises;

public class TenHelloWorlds {
    public static void main(String[] args) {
        int n = 10;
        for (int i = 1; i <= n; i++) {
            System.out.println("Hello, World ");
        }
        System.out.println();
    } // Prints 10 "Hello, World"s using for loop
}
